# 绿色字体
print('\033[1;32m'+'green'+'\033[0m')

# 蓝色字体
print('\033[1;34m'+'blue'+'\033[0m')

# 黄字下划线
print('\033[4;33m'+'yellow'+'\033[0m')

# 红底黑字
print('\033[1;30;41m'+'black'+'\033[0m')

# 白底黑字
print('\033[1;30;47m'+'white'+'\033[0m')

